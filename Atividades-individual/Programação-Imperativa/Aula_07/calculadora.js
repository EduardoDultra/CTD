
function soma(num1 = 0, num2 = 0) {
    return num1 + num2;
}

function subtracao(num1 = 0, num2 = 0) {
    return num1 - num2;
}

function multiplicacao(num1 = 1, num2 = 1) {
    return num1 * num2;
}

function divisao(num1 = 1, num2 = 1) {
    return num1 / num2;
}


